function offsetForProfile(profileId: string, n: number): number {
  let h = 0;
  for (let i = 0; i < profileId.length; i++) h = (h * 31 + profileId.charCodeAt(i)) | 0;
  return ((h % n) + n) % n;
}

export function heartMessageForProfile(profileId: string, date: Date = new Date()): string {
  const dayNumber = Math.floor(date.getTime() / 86_400_000);
  const n = HEART_MESSAGES.length;
  // Offset differs from the kindness offset (different multiplier phase via
  // reversed id) so the two daily texts don't march in lockstep.
  const idx = ((dayNumber + offsetForProfile([...profileId].reverse().join(""), n)) % n + n) % n;
  return HEART_MESSAGES[idx];
}
